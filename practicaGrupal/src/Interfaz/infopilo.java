    public void mostrarPilo(){
       JTextArea area = new JTextArea(); 
       String Salida= "";
        for (int i = 0; i < pilotos.size(); i++){ 
            Salida +="DATOS DEL PILOTO"+"\n";
            Salida += "Nombre y apellidos del piloto: "+pilotos.get(i).getNombre()+ pilotos.get(i).getApellidos() +"\n"+
                    " Edad: "+pilotos.get(i).getEdad()+"\n"+
                    " Altura: "+pilotos.get(i).getAltura()+"\n"+
                    " Peso: "+pilotos.get(i).getPeso()+"\n"+
                    " Sueldo: "+pilotos.get(i).getSueldo()+"\n"+
                    " Valoracion: "+pilotos.get(i).getValoracionGlobal();
            Salida += "\n"+"\n";
            jTextArea1.setText(Salida);
             }
        } 

